    @Override
    public void actionPerformed(ActionEvent e) {
        Object o = e.getSource();
        ClientContext c = ClientContext.instance();
        WorldPoint loc = c.players.getLocal().getLocation();
        if (o.equals(btn_Add)) {
            pathList.add("" + loc.x + ", " + loc.y + ", " + loc.plane);
            System.out.println(pathList.get(pathList.size()-1));
        } else if (o.equals(btn_Del)) {
            pathList.remove(pathList.size()-1);
        } else if (o.equals(btn_Gen)) {
            txt_Out.setText("public WorldPoint[] path = new WorldPoint[] {");
            for (int i = 0; i < pathList.size(); i++) {
                txt_Out.setText(txt_Out.getText() + "\n\tnew WorldPoint(" + pathList.get(i) + "),");
            }
            txt_Out.setText(txt_Out.getText() + "\n };");

        }

        txt_Total.setText("Total Tiles in Paths:" + pathList.size());
    }
